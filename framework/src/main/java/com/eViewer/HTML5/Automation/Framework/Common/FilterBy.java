package com.eViewer.HTML5.Automation.Framework.Common;

public enum FilterBy {

	SELECT_ALL("select all"), PAGE_RANGE("page range"), COMMENT("comment"), BOOKMARK("bookmark"), ANNOTATION(
			"annotation"), LAST_COMMENT(
					"last comment"), CUSTOM("custom"), CURRENT_PAGE("current page"), CURRENT_DOCUMENT("current doc");

	private final String value;

	private FilterBy(String value) {
		this.value = value;
	}

	public String getValue() {
		return value;
	}

}
