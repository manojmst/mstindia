package com.eViewer.HTML5.Automation.Framework.Common;

public enum FileExtensionHelper {

	PDF(".pdf"), PDFA(".pdfa"), TIFF(".tif"), TXT(".txt"), XLS(".xls"), XLSX(".xlsx"), DOC(".doc"), DOCX(".docx"), PPT(
			".ppt"), PPTX(".pptx"), BMP(".bmp"), PNG(".png"), JPG(".jpg");

	private final String value;

	private FileExtensionHelper(String value) {
		this.value = value;
	}

	public String getValue() {
		return value;
	}

}
